    public void mostrarDatos(){
    modelo.setNumRows(contactos.size());
    for(int i=0; i<contactos.size(); i++){
        modelo.setValueAt(contactos.get(i).getCodigo(), i, 0);
        modelo.setValueAt(contactos.get(i).getNombre(), i, 1);
        modelo.setValueAt(contactos.get(i).getApellido(), i, 2);
        modelo.setValueAt(contactos.get(i).getTelefono(), i, 3);
        modelo.setValueAt(contactos.get(i).getCorreo(), i, 4);
        
    }
    jtDatos.setModel(modelo);
}

    public void buscarContacto(String buscar) {
    DefaultTableModel modeloBusqueda = new DefaultTableModel();
    modeloBusqueda.addColumn("CODIGO");
    modeloBusqueda.addColumn("NOMBRE");
    modeloBusqueda.addColumn("APELLIDO");
    modeloBusqueda.addColumn("TELEFONO");
    modeloBusqueda.addColumn("CORREO");

    for (Contacto c : contactos) {
        // Buscar por nombre o apellido
        if (c.getNombre().toLowerCase().contains(buscar.toLowerCase()) ||
            c.getApellido().toLowerCase().contains(buscar.toLowerCase())) {
            Object[] fila = new Object[5];
            fila[0] = c.getCodigo();
            fila[1] = c.getNombre();
            fila[2] = c.getApellido();
            fila[3] = c.getTelefono();
            fila[4] = c.getCorreo();
            modeloBusqueda.addRow(fila);
        }
    }

    // Mostrar los resultados de la búsqueda
    jtDatos.setModel(modeloBusqueda);
}


