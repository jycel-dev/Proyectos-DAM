    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
    
        
        if(!tfNombre.getText().equals("")&& !tfApellido.getText().equals("") 
                && !tfTelefono.getText().equals("") && !tfCorreo.getText().equals("")){
           Contacto c = new Contacto();
           c.setCodigo(codigo);
           codigo++;
           c.setNombre(tfNombre.getText());
           c.setApellido(tfApellido.getText());
           c.setTelefono(tfTelefono.getText());
           c.setCorreo(tfCorreo.getText());
           contactos.add(c);
           tfNombre.setText("");
           tfApellido.setText("");
           tfTelefono.setText("");
           tfCorreo.setText("");
           
           mostrarDatos();
        }else{
                JOptionPane.showInternalMessageDialog(null, "INGRESE LOS DATOS SOLICITADOS");
                }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(jtDatos.getSelectedRow()<0) {
        JOptionPane.showMessageDialog(null, "SELECCIONE EL REGISTRO A ELIMINAR");
        }else{
            int filaSeleccionada = jtDatos.getSelectedRow();
            
        contactos.remove(filaSeleccionada);
        for (int i = filaSeleccionada; i < contactos.size(); i++) {
            Contacto c = contactos.get(i);
            c.setCodigo(c.getCodigo() - 1);}
        mostrarDatos();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

